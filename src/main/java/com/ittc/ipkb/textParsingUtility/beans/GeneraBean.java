package com.ittc.ipkb.textParsingUtility.beans;

/**
 * 
 * @author rsompalli
 * 
 * Java bean that holds the info of the Genera
 *
 */
public class GeneraBean 
{
	private String generaName;
	private String phylumName;
	private String subPhylumName;
	private String className;
	private String subClassName;
	private String orderName;
	private String subOrderName;
	private String superFamilyName;
	private String familyName;
	private String subFamilyName;
	private String authorInfo;
	private String referenceInfo;
	private String description;
	private String timePeriod;
	private String figureIndex;
	
	public String getGeneraName() {
		return generaName;
	}
	public void setGeneraName(String generaName) {
		this.generaName = generaName;
	}
	public String getPhylumName() {
		return phylumName;
	}
	public void setPhylumName(String phylumName) {
		this.phylumName = phylumName;
	}
	public String getSubPhylumName() {
		return subPhylumName;
	}
	public void setSubPhylumName(String subPhylumName) {
		this.subPhylumName = subPhylumName;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getSubClassName() {
		return subClassName;
	}
	public void setSubClassName(String subClassName) {
		this.subClassName = subClassName;
	}
	public String getOrderName() {
		return orderName;
	}
	public void setOrderName(String orderName) {
		this.orderName = orderName;
	}
	public String getSubOrderName() {
		return subOrderName;
	}
	public void setSubOrderName(String subOrderName) {
		this.subOrderName = subOrderName;
	}
	public String getSuperFamilyName() {
		return superFamilyName;
	}
	public void setSuperFamilyName(String superFamilyName) {
		this.superFamilyName = superFamilyName;
	}
	public String getFamilyName() {
		return familyName;
	}
	public void setFamilyName(String familyName) {
		this.familyName = familyName;
	}
	public String getSubFamilyName() {
		return subFamilyName;
	}
	public void setSubFamilyName(String subFamilyName) {
		this.subFamilyName = subFamilyName;
	}
	public String getAuthorInfo() {
		return authorInfo;
	}
	public void setAuthorInfo(String authorInfo) {
		this.authorInfo = authorInfo;
	}
	public String getReferenceInfo() {
		return referenceInfo;
	}
	public void setReferenceInfo(String referenceInfo) {
		this.referenceInfo = referenceInfo;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getTimePeriod() {
		return timePeriod;
	}
	public void setTimePeriod(String timePeriod) {
		this.timePeriod = timePeriod;
	}
	public String getFigureIndex() {
		return figureIndex;
	}
	public void setFigureIndex(String figureIndex) {
		this.figureIndex = figureIndex;
	}
}
